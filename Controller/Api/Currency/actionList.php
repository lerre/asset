    public function main()
    {
        if ($this->isPost()) {
            $this->insertData();
        }

        $output = [];

        $dbCurrency = new Currency();
        $data = $dbCurrency->getList('coin_id,coin,coin_name');
        $result = [];
        if (!empty($data)) {
            foreach ($data as $k => $v) {
                $str = '';
                if (!empty($v['coin'])) $str = $v['coin'];
                if (!empty($v['coin_name'])) $str .= ' - ' . $v['coin_name'];
                if (!empty($v['coin_id'])) $str .= ' - ' . $v['coin_id'];
                $result[$k] = $str;
            }
        }

        $output['list'] = array_values($result);

        $this->success($output);
    }

    private function insertData()
    {
        $data = $this->getData();

        $currencyDb = new Currency();

        foreach ($data as $k => $v) {
            $res = [];
            $res['name'] = $v[0];
            $res['short_name'] = $v[1];
            $res['zhong_name'] = $v[2];
            $currencyDb->insertCurrency($res);
        }

        $this->success();
    }

